<template>
    <section>
        COACH LIST
    </section>
    <ul v-if="hasCoaches">
        <li v-for="coach in filteredCoaches" :key="coach.id">{{coach.firstName}}</li>
    </ul>
    <p v-else>No coaches Found!</p>
</template>

<script>
export default {
    computed: {
        filteredCoaches () {
            return this.$store.getters['coaches/coaches'];
        },
        hasCoaches() {
            return this.$store.getters['coaches/hasCoaches'];
        }
    }
}
</script>